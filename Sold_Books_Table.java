        private void SaveToExcel(){
        try{
           JFileChooser jFileChooser = new JFileChooser();
           jFileChooser.showSaveDialog(this);
           File saveFile = jFileChooser.getSelectedFile();
           
           if(saveFile != null){
               saveFile = new File(saveFile.toString()+".xlsx");
               Workbook wb = new XSSFWorkbook();
               Sheet sheet = wb.createSheet("customer");
               
               Row rowCol = sheet.createRow(0);
               for(int i=0;i<jTableSoldBooks.getColumnCount();i++){
                   Cell cell = rowCol.createCell(i);
                   cell.setCellValue(jTableSoldBooks.getColumnName(i));
               }
               
               for(int j=0;j<jTableSoldBooks.getRowCount();j++){
                   Row row = sheet.createRow(j+1);
                   for(int k=0;k<jTableSoldBooks.getColumnCount();k++){
                       Cell cell = row.createCell(k);
                       if(jTableSoldBooks.getValueAt(j, k)!=null){
                           cell.setCellValue(jTableSoldBooks.getValueAt(j, k).toString());
                       }
                   }
               }
               FileOutputStream out = new FileOutputStream(new File(saveFile.toString()));
               wb.write(out);
               wb.close();
               out.close();
               JOptionPane.showMessageDialog(null, "File Has Been Saved");
           }else{
               JOptionPane.showMessageDialog(null,"Error Please Try Again Later");
           }
       }catch(FileNotFoundException e){
           System.out.println(e);
       }catch(IOException io){
           System.out.println(io);
       }
    }
    
