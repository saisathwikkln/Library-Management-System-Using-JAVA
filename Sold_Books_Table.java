    private void LoadAllSoldBooks(){ 
        try {
            ResultSet rs;
            conn con = new conn();
            String sql ="SELECT * FROM soldbooks";
            DefaultTableModel modele = (DefaultTableModel) jTableSoldBooks.getModel();        
            PreparedStatement st = con.c.prepareStatement(sql);
            rs = st.executeQuery();
            while(rs.next()){
            Object o[]= {rs.getString("sale_id"), rs.getString("bookId"), rs.getString("bookName"), rs.getString("publisher")
                    ,rs.getString("price"), rs.getString("Numbers"), rs.getString("discount")
                    ,rs.getString("total") ,rs.getString("date")};
            if(jTableSoldBooks.getRowCount()!= Number_Of_Sold_Books())    
                modele.addRow(o);                
            }
                st.close();
                con.c.close();
        } catch(SQLException ex) {
              JOptionPane.showMessageDialog(this, "Error");
            }
    }  
