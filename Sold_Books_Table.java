                     String Sale_Id = jTableSoldBooks.getValueAt(jTableSoldBooks.getSelectedRow(), 0).toString();
                     String Book_Id = jTableSoldBooks.getValueAt(jTableSoldBooks.getSelectedRow(), 1).toString();

